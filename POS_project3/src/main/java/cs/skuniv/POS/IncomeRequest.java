package cs.skuniv.POS;

public class IncomeRequest {
	
	private String code;
	private int stock;
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public int getStock() {
		return stock;
	}
	
	public void setStock(int stock) {
		this.stock = stock;
	}	

}
